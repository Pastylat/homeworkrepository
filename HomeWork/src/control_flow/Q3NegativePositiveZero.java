

package control_flow;

public class Q3NegativePositiveZero {
	
	public static void main(String[] args) {
		
// Create a program that reads an integer and determines:
// whether it's Positive, Negative, or Zero. Print the result.
		
		double number = -0.1;
		
		
		if(number > 0) {
			System.out.println("the integer is: Positive");
		}
		else if (number < 0) {
			System.out.println("the integer is: Negative");
		}
		else {
			System.out.println("the integer is: Zero");
		}
		
		
	}
	
}
